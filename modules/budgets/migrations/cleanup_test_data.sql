-- ============================================================================
-- Скрипт очистки тестовых данных модуля бюджетов
-- ============================================================================
-- ВНИМАНИЕ: Этот скрипт удаляет ВСЕ данные, созданные в модуле бюджетов
-- Используйте только для очистки тестовой среды!
-- ============================================================================

-- Шаг 1: Удаляем все отчёты (work_logs), привязанные к бюджетам
DELETE FROM work_logs
WHERE budget_id IS NOT NULL;

-- Шаг 2: Удаляем все версии бюджетов
DELETE FROM budget_versions;

-- Шаг 3: Удаляем все бюджеты
DELETE FROM budgets;

-- Шаг 4: Удаляем все типы бюджетов
DELETE FROM budget_types;

-- Готово! Модуль бюджетов полностью очищен.
